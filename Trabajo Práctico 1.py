def Slicing_categories(category):
    """
    This function takes a set category of data and separates it between name and data

    :Parameters:
    category (str) : The set category of the function to separate

    :Returns:
    category_name (str) : The name of the category
    category_data (str) : The data corresponding to set category
    """
    category_name = ""
    category_data = ""
    validator = True
    for letter2 in category:
        if letter2 == ":":
            validator = False
        elif validator:
            category_name += letter2
        else:
            category_data += letter2
    return category_name,category_data


