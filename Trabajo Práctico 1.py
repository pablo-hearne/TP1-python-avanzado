"""
6.
Crea una función llamada
fibonacci(n)
que reciba como parámetro un número entero n y devuelva una lista con los primeros n números de la serie de Fibonacci. 
La serie de Fibonacci es una secuencia en la que cada número se obtiene sumando los dos anteriores.
"""

def Fibonacci(number:int) -> list:
    """
    This function takes an integral number(n) a gives back the fibonacci sequence 'n' times

    :Parameters: 
    number(int): the amount of numbers of the fibonacci sequence

    :Return:
    fibo_list(list[int]): the fibonacci sequence up until 'n' numbers
    """
    fibo_list = []
    for i in range(number):
        fibo_number = i + (i+1)
        fibo_list.append(fibo_number)

    return fibo_list

