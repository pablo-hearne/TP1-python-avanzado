"""
7.
Crea una función recursiva llamada
suma_recursiva
que reciba un número
n
y devuelva la suma de los primeros
n
números naturales. Ejemplo:
suma_recursiva(5)
debe devolver 15 (1+2+3+4+5).
"""
def Suma_recursiva(n:int,result=0) -> int:
    """
    This function takes an integrer and sums it up with all its previous numbers until 0    
    
    :Parameters:
    n(int) : the number in question

    :Results:
    result(int) : the result of the mathemathical algorithm
    """
    result += n
    if (n-1) == 0:
        return result
    return Suma_recursiva(n-1,result)

